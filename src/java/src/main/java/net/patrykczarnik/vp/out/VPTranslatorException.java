package net.patrykczarnik.vp.out;

public class VPTranslatorException extends Exception {

	public VPTranslatorException() {
		super();
	}

	public VPTranslatorException(String message, Throwable cause) {
		super(message, cause);
	}

	public VPTranslatorException(String message) {
		super(message);
	}

	public VPTranslatorException(Throwable cause) {
		super(cause);
	}

}
